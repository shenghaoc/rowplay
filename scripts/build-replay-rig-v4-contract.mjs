import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { relative, resolve } from "node:path";
import { validateV4Asset } from "./validate-replay-rig-v4.mjs";
import {
  V4_ASSET_FILENAME,
  V4_BONE_DEFINITIONS,
  V4_BONE_NAMES,
  V4_CLIP_NAMES,
  V4_CONTACT_OFFSETS,
  V4_CONTACT_ROLES,
  V4_CYCLE_SECONDS,
  V4_DRIVE_END,
  V4_PHASE_SCHEMAS,
  V4_RIG_NAME,
  V4_ROOT_NAME,
} from "../src/lib/replay/rigV4.ts";

export const V4_USDZ_FILENAME = "rowplay-athlete-v4.usdz";
export const V4_CONTRACT_FILENAME = "rowplay-athlete-v4.contract.json";
export const V4_CONTRACT_SCHEMA = "rowplay.replay.athlete.v4";
export const V4_CONTRACT_VERSION = 1;

const DEFAULT_OUTPUT = `static/replay-assets/${V4_CONTRACT_FILENAME}`;
const GLB_PATH = `static/replay-assets/${V4_ASSET_FILENAME}`;
const USDZ_PATH = `static/replay-assets/${V4_USDZ_FILENAME}`;

function sha256(bytes) {
  return createHash("sha256").update(bytes).digest("hex");
}

async function artifact(filename, path) {
  const bytes = await readFile(path);
  return {
    filename,
    byteCount: bytes.byteLength,
    sha256: sha256(bytes),
  };
}

function clipContracts() {
  return Object.entries(V4_CLIP_NAMES).map(([sport, name]) => ({
    sport,
    name,
    durationSeconds: V4_CYCLE_SECONDS,
    driveEnd: V4_DRIVE_END[sport],
    phaseLandmarks: V4_PHASE_SCHEMAS[sport],
  }));
}

function boneContracts() {
  return V4_BONE_DEFINITIONS.map((bone) => ({
    name: bone.name,
    parent: bone.parent ?? null,
    restLocalTransform: {
      translation: bone.position,
      rotationQuaternion: [0, 0, 0, 1],
      scale: [1, 1, 1],
    },
  }));
}

function contactContracts() {
  return Object.keys(V4_CONTACT_OFFSETS).map((boneName) => ({
    bone: boneName,
    role: V4_CONTACT_ROLES[boneName],
    localOffset: V4_CONTACT_OFFSETS[boneName],
  }));
}

export async function buildV4Contract(outputPath = DEFAULT_OUTPUT) {
  const glb = await validateV4Asset(GLB_PATH);
  const contract = {
    schema: V4_CONTRACT_SCHEMA,
    schemaVersion: V4_CONTRACT_VERSION,
    canonicalSourceModule: "scripts/build-replay-athlete-v4-blender.py",
    canonicalRigModule: "src/lib/replay/rigV4.ts",
    webRuntimeArtifact: await artifact(V4_ASSET_FILENAME, GLB_PATH),
    nativeDerivativeArtifact: await artifact(V4_USDZ_FILENAME, USDZ_PATH),
    coordinateSystem: {
      units: "metres",
      upAxis: "+Y",
      forwardAxis: "+Z",
      handedness: "right-handed",
      rootOrientation: {
        translation: [0, 0, 0],
        rotationQuaternion: [0, 0, 0, 1],
        scale: [1, 1, 1],
      },
    },
    mesh: {
      rootName: V4_ROOT_NAME,
      meshName: V4_RIG_NAME,
      skinnedMeshes: 1,
      primitives: 1,
      vertices: glb.vertices,
      triangles: glb.triangles,
      topologyComponents: glb.components,
      skins: 1,
      materials: glb.materials,
      materialSlots: 1,
    },
    materialDetail: {
      glbEmbeddedTextures: 0,
      uvCoordinateSet: "TEXCOORD_0",
      webRuntime:
        "Per-instance deterministic UV albedo, normal, roughness, and relief maps: 32px Medium, 64px High, 96px Ultra; no external image or texture request.",
    },
    bones: {
      // Keep the semantic interface stable for existing consumers. Visual
      // helpers are documented separately and can grow without changing replay
      // motion/contact vocabulary.
      count: V4_BONE_NAMES.length,
      orderedNames: V4_BONE_NAMES,
      semanticCount: V4_BONE_NAMES.length,
      semanticOrderedNames: V4_BONE_NAMES,
      totalCount: glb.bones,
      helperCount: glb.helperBones,
      helperNames: glb.helperBoneNames,
      hierarchy: boneContracts(),
      helpers: glb.helpers,
    },
    animation: {
      clips: clipContracts(),
      clipTracks: glb.clipTracks,
    },
    contacts: contactContracts(),
    surfaces: [
      { role: "athlete-fabric", source: "vertex-color kit regions" },
      { role: "athlete-skin", source: "vertex-color skin regions" },
      { role: "athlete-shorts", source: "vertex-color pelvis/thigh kit regions" },
      { role: "athlete-footwear", source: "vertex-color shoe regions" },
      { role: "athlete-hair", source: "vertex-color hair regions" },
      { role: "athlete-trim", source: "vertex-color jersey/shoe accent regions" },
    ],
    provenance: {
      owner: "rowplay",
      licence: "MIT",
      source: "repository-authored Blender 5 production skinned athlete",
      authoringTool: "Blender 5.2 LTS",
      authoringScript: "scripts/build-replay-athlete-v4-blender.py",
      rigAndClipSource: "src/lib/replay/rigV4.ts",
      forbiddenSources: [
        "downloaded model",
        "third-party character",
        "scan",
        "likeness",
        "avatar generator",
        "external texture",
        "user image",
      ],
      nativeDerivative: "Generated by Blender 5.2 from the canonical GLB; do not remodel.",
    },
    validation: {
      buildCommands: [
        "pnpm run build:replay-rig-v4",
        "pnpm run build:replay-rig-v4-usdz",
        "pnpm run build:replay-rig-v4-contract",
      ],
      validationCommands: [
        "pnpm run validate:replay-assets",
        "pnpm run check",
        "pnpm run validate:locales",
        "pnpm run test:e2e:smoke",
        "git diff --check",
      ],
    },
  };
  const serialized = `${JSON.stringify(contract, null, 2)}\n`;
  await writeFile(outputPath, serialized);
  return contract;
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const output = resolve(process.argv[2] ?? DEFAULT_OUTPUT);
  const contract = await buildV4Contract(output);
  const displayPath = relative(process.cwd(), output) || output;
  console.log(
    `wrote ${displayPath}: ${contract.webRuntimeArtifact.byteCount} GLB bytes, ${contract.nativeDerivativeArtifact.byteCount} USDZ bytes, contract v${contract.schemaVersion}`,
  );
}
